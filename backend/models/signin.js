const mongoose1=require('mongoose');

const Cart=new mongoose.Schema({
    position: {
        type: String,
        required: true
    },
    name: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    quantity: {
        type: Number
    }
});

const user=new mongoose1.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    mobile: {
        type: Number,
        required: true
    },
    password: {
        type: String,
        required: true
    },
   cart: [Cart],
});

module.exports=mongoose1.model('User',user);